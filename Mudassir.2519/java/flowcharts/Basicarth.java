import java.util.*;
public class Basicarth {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int A = sc.nextInt();
        int B = sc.nextInt();
        System.out.println(A + B);
        System.out.println(A - B);
        System.out.println(A * B);
        System.out.println((double) A / B);
        System.out.println(A / B);
        System.out.println(A % B);
    }
}
