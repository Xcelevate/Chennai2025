import java.util.Scanner;

public class FCP004 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        long A = sc.nextLong();
        long B = sc.nextLong();

        System.out.println(A + B);
        System.out.println(A - B);
        System.out.println(A * B);
        System.out.println((double) A / B);
        System.out.println(A / B);
        System.out.println(A % B);
    }
}
    