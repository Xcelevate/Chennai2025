import java.util.Scanner;
public class FCP088 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int N = sc.nextInt();
        for (int i = 1; i <= N; i++) {
            for (int j = 1; j <= (N - i) * 2; j++) {
                System.out.print(" ");
            }
            for (int j = N; j > N - i; j--) {
                System.out.print(j + " ");
            }
            for (int j = N - i + 2; j <= N; j++) {
                System.out.print(j + " ");
            }
			System.out.println();
        }
        for (int i = N - 1; i >= 1; i--) {
            for (int j = 1; j <= (N - i) * 2; j++) {
                System.out.print(" ");
            }
            for (int j = N; j > N - i; j--) {
                System.out.print(j + " ");
            }
            for (int j = N - i + 2; j <= N; j++) {
                System.out.print(j + " ");
            }
			System.out.println();
        }
    }
}
