import java.util.Scanner;
public class FCP040 {
    public static void main(String[] args) {
        Scanner scn = new Scanner(System.in);
        int A = scn.nextInt();

        int max = 0;
        int secondMax = 0;

        for (int i = 0; i < A; i++) {
            int num = scn.nextInt();

            if (num > max) {
                secondMax = max;
                max = num;
            } else if (num > secondMax && num < max) {
                secondMax = num;
            }
        }

        System.out.println(secondMax);
    }
}

