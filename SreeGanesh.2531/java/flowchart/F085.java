import java.util.Scanner;

public class F085 {
    public static void main(String[] args) {
        Scanner scn = new Scanner(System.in);
        int N = scn.nextInt();

        for (int i = 1; i <= N; i++) {
            for (int j = 1; j <= i; j++) {
                System.out.print(j);
                if (j != i || i - 1 >= 1) System.out.print(" ");
            }
            for (int j = i - 1; j >= 1; j--) {
                System.out.print(j);
                if (j != 1) System.out.print(" ");
            }
            System.out.println();
        }

        scn.close();
    }
}
