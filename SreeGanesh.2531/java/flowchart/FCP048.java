import java.util.Scanner;
public class FCP048{
      public static void main(String[] args){
	  Scanner scn=new Scanner(System.in);
	  int n=scn.nextInt();
	  int m=scn.nextInt();
	  int d=0;
	  int s=0;
	  int c=0;
	  int temp=n;
	  if(n==0){
	  n=temp;
	  d=n%10;
	  s=s+d*c;
	  }
	  else{
	  d=n%10;
	  n=n/10;
	  c++;
	  }
	  if(n>=m){
	  n++;
	  }
	  System.out.println(n);
	  
	  }
	  }
