import java.util.Scanner;
public class FCP036 {
    public static void main(String[] args) {
        Scanner scn = new Scanner(System.in);
        int M = scn.nextInt();
        int N = scn.nextInt();
        int m1 = 0;
        int m2 = 0;
        if (M > N) {
            m1 = M;
            m2 = N;
        } else {
            m1 = N;
            m2 = M;
        }
        int lcm = m1;
        while (lcm % m2 != 0) {
            lcm += m1;
        }
        int hcf = (M * N) / lcm;
        System.out.println(hcf);
        System.out.println(lcm);
    }
}
