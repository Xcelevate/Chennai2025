import java.util.Scanner;
public class FCP63{
    public static void main(String[] args) {
        Scanner scn = new Scanner(System.in);
        int N = scn.nextInt();  
        int[] A = new int[N];
        for (int i = 0; i < N; i++) {
            A[i] = scn.nextInt();
        }
		int m=0;
		int c=0;
		for(int i=0;i<N;i++){
		if(A[i]==1){
		c++;
		if(c>m){
			m=c;
		}
		}
		else{
		c=0;
		}
		}
		
		System.out.println(m);
		}
		}
