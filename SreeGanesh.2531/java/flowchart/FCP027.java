import java.util.Scanner;
public class FCP027{
public static void main(String[] args){
Scanner scn=new Scanner(System.in);
int n=scn.nextInt();
double d=0.0;
for(int i=1;i<=n;i++){
d=d+1.0/i;
}
System.out.printf("%.2f",d);
}
}
