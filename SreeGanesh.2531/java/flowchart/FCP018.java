import java.util.Scanner;
public class FCP018{
public static void main(String[] args){
Scanner scn=new Scanner(System.in);	
int BS=scn.nextInt();
double GS=0;
double HRA=0;
double DA=0;
if(BS<=10000){
HRA=0.20*BS;
DA=0.80*BS;
}
else if(BS<=20000){
HRA=0.25*BS;
DA=0.90*BS;
}
else{
HRA=0.30*BS;
DA=0.95*BS;
}
GS=HRA+DA+BS;
System.out.println((int)GS);

}
}
