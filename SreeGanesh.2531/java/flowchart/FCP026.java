import java.util.Scanner;
public class FCP026{
public static void main(String[] args){
Scanner scn=new Scanner(System.in);
int M=scn.nextInt();
int N=scn.nextInt();
long sum=0;
for( int i=M;i<=N;i++){
sum=(long)sum+i;
}
System.out.println(sum);
}
}


