import java.util.Scanner;

public class AlternatingSeries {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        int N = sc.nextInt();       
        double X = sc.nextDouble();

        double sum = 0;

        for (int i = 0; i < N; i++) {
            int e = 2 * i + 1;              
            double term = Math.pow(X, e) / e;

            if (i % 2 == 0) {
                sum += term; 
            } else {
                sum -= term; 
            }
        }

        System.out.printf("%.6f\n", sum);

    }
}
