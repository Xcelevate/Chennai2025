import java.util.Scanner;
public class CountTriplets{
public static void main(String[]args){
Scanner scn=new Scanner(System.in);
int N=scn.nextInt();
long t=0;
for(int a=1;a<=N-2;a++){
   for(int b=a+1;b<=N-1;b++){
   int c=N-a-b;
   if(c>b && c<=N){
   t++;
   }
   }
   }
   System.out.println(t);
   }
   }
   
