import java.util.Scanner;
public class AlternateOE {
public static void main(String[]args){
Scanner scn=new Scanner(System.in);
int N=scn.nextInt();
int x=0;
int a=0;
int b=0;
if(N==0){
System.out.println("Yes");
return;
}
while(N>0){
x=N%10;
if(x%2==0){
a=1;
}
else{
a=2;
}
if(b!=0 && a==b){
System.out.println("No");
return;
}
b=a;
N=N/10;
}
System.out.println("Yes");
}
}

