import java.util.Scanner;
public class PS{
public static void main(String[]args){
Scanner scn=new Scanner(System.in);
int N=scn.nextInt();
int i=1;
while(true){
long d=i*i;
if(d>N){
	System.out.println(d);
	break;
}
	i++;
}
}
}

