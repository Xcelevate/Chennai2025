import java.util.Scanner;
public class Gap{
public static void main(String[]args){
Scanner scn=new Scanner(System.in);
int N=scn.nextInt();
int s=1;
int c=1;
for(int i=1;i<=N;i++){
System.out.print(s+" ");
s=s+c;
c++;
}
}
}
