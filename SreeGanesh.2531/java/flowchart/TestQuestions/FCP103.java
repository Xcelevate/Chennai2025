import java.util.Scanner;
public class CountEven{
public static void main(String[]args){
Scanner scn=new Scanner(System.in);
int N=scn.nextInt();
int c=0;
for(int i=2;i<=N;i+=2){
	int a=i;
	boolean alleven=true;
	while(a>0){
		int d=a%10;
		if(d%2!=0){
		alleven=false;
		break;
		}
	a=a/10;
	}
	if(alleven){
		c++;
	}
}
System.out.println(c);
}
}

		
	
