import java.util.Scanner;
public class Balanced {
public static void main(String[]args){
Scanner scn=new Scanner(System.in);
int N=scn.nextInt();
int m=N/2;
int s1=0;
int s2=0;
for(int i=0;i<m;i++){
int a=scn.nextInt();
s1=s1+a;
}
for(int i=0;i<m;i++){
int b=scn.nextInt();
s2=s2+b;
}
if(s1==s2){
System.out.println("Balanced");
}
else{
System.out.println("Unbalanced");
}
}
}
