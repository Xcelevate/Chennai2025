import java.util.Scanner;
public class FCP056 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int N=sc.nextInt();
	int s1=0;
	int s2=0;
        int[] A = new int[N];
		for(int i=0;i<N;i++){
		A[i]=sc.nextInt();
		}
		for(int j=0;j<N;j++){
		if(A[j]%2==0){
		s2=s2+A[j];
		}
		else{
		s1=s1+A[j];
		}
		}
		System.out.println(s1);
		System.out.println(s2);
		}
		}
