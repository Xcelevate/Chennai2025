import java.util.Scanner;
public class FCP033 {
    public static void main(String[] args) {
        Scanner scn = new Scanner(System.in);
        int N = scn.nextInt();
		int i=N;
        int s = 0;

        while (N > 0) {
            int r = N % 10;
            s = s*10 + r;
            N = N / 10;
        }
        if(s==i){
        System.out.println("Yes");
    }
	   else{
	    System.out.println("No");
}
	}
}

