import java.util.Scanner;
public class FCP057 {
    public static void main(String[] args) {
        Scanner scn = new Scanner(System.in);
        int N = scn.nextInt();
        int M = scn.nextInt();
        int[] A = new int[N];
        for (int i = 0; i < N; i++) {
            A[i] = scn.nextInt();
        }
        int count = 0;
        for (int i = 0; i < N; i++) {
            if (A[i] == M) {
                count++;
            }
        }
        System.out.println(count);
    }
}
