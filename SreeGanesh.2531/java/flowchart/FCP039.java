import java.util.Scanner;
public class FCP039 {
    public static void main(String[] args) {
        Scanner scn = new Scanner(System.in);
        int N = scn.nextInt();
		int m1=0;
		int m2=0;
		int A=scn.nextInt();
		m1=A;
		m2=A;
		for(int i=1;i<N;i++){
		int num=scn.nextInt();
		if(num>m1){
		m1=num;
		}
		if(num<m2){
		m2=num;
		}
		}
		System.out.println(m1);
		System.out.println(m2);
		
		}
		}
