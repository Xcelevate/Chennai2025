import java.util.Scanner;

public class F086 {
    public static void main(String[] args) {
        Scanner scn = new Scanner(System.in);
        int N = scn.nextInt();

       
        for (int i = 1; i <= N; i++) {
            for (int j = 1; j <= 2 * i - 1; j++) {
                System.out.print("*");
                if (j < 2 * i - 1) {
                    System.out.print(" ");
                }
            }
            System.out.println();
        }

      
        for (int i = N - 1; i >= 1; i--) {
            for (int j = 1; j <= 2 * i - 1; j++) {
                System.out.print("*");
                if (j < 2 * i - 1) {
                    System.out.print(" ");
                }
            }
            System.out.println();
        }

        scn.close();
    }
}
