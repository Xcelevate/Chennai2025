import java.util.Scanner;
public class FCP047 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int a = sc.nextInt();
        int b = sc.nextInt();
        if (a > b) {
            a = a + b;
            b = a - b;
            a = a - b;
        }
        int i = 1;
        int answer = 1;
        while (i <= a) {
            if (a % i == 0 && b % i == 0) {
                answer = i; 
            }
            i++;
        }
        System.out.println(answer);
    }
}
