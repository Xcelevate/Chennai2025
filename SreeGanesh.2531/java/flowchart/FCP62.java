import java.util.Scanner;
public class FCP62{
    public static void main(String[] args) {
        Scanner scn = new Scanner(System.in);
        int N = scn.nextInt();  
        int[] A = new int[N+1];
        for (int i = 0; i < N; i++) {
            A[i] = scn.nextInt();
        }
		int K=scn.nextInt();
		int m=0;
		while(m!=K){
		int z = A[0];
		for(int j=0;j<N-1;j++){
		A[j]=A[j+1];
		}
		A[N-1]=z;
		m+=1;
		}
		for(int d=0;d<N;d++){
		System.out.println(A[d]);
		}
		}
		}
