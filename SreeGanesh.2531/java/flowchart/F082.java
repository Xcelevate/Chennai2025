import java.util.Scanner;

public class F082 {
    public static void main(String[] args) {
        Scanner scn = new Scanner(System.in);
        int N = scn.nextInt(); 
        for (int i = 1; i <= N; i++) {
          for (int j = 1; j <= N - i + 1; j++) {
                System.out.print("* ");  
            }
            
            System.out.println();
        }

        scn.close();
    }
}import java.util.Scanner;

