import java.util.Scanner;

public class FCP091 {
    public static void main(String[] args) {
        Scanner scan = new Scanner(System.in);
        int n = scan.nextInt();
        int r = n;
        int a = r;
        int y = n - 1;
        for (int i = 0; i < n; i++) {
            for (int k = 0; k < i; k++) {
                System.out.print(" ");
            }
            System.out.print(a);

            for (int j = 0; j < y; j++) {
                System.out.print("_" + r);
                r--;
            }
            a--;
            r = n;
            y--;
            System.out.println();
        }
        a=2;
        y=1;
        int m=1;
        for(int i=0;i<n-1;i++){
            for (int k = n-1; k >m; k--) {
                System.out.print(" ");
            }
            System.out.print(a);
            for (int j = 0; j < y; j++) {
                System.out.print("_" + r);
                r--;
            }
            m++;
            a++;
            y++;
            r=n;
            System.out.println();
        }
        
    }
}import java.util.Scanner;

