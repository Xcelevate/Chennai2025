import java.util.Scanner;
public class F37{
public static void main(String[] args){
Scanner scn=new Scanner(System.in);
int N=scn.nextInt();
int sum=0;
if(N<1){
System.out.println("No");
return;
}
for(int i=1;i<N;i++){
if(N%i==0){
sum+=i;
}
}
if(sum==N){
System.out.println("Yes");
}
else{
System.out.println("No");
}
}
}
