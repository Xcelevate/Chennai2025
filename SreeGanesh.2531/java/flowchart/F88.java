import java.util.Scanner;

public class F88 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int N = sc.nextInt();
        int totalRows = 2 * N - 1;

        for (int i = 1; i <= totalRows; i++) {
            int limit;
            if (i <= N) {
                limit = i;
            } else {
                limit = 2 * N - i;
            }

            int spaces = N - limit;

            // Print leading spaces
            for (int s = 0; s < spaces; s++) {
                System.out.print("  ");
            }

            // Print descending part
            for (int j = N; j > N - limit; j--) {
                System.out.print(j + " ");
            }

            // Print ascending part
            for (int j = N - limit + 2; j <= N; j++) {
                System.out.print(j + " ");
            }

            System.out.println();
        }
    }
}
