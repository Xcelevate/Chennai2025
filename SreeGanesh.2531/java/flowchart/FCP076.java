import java.util.Scanner;
public class FCP076 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
		int N=sc.nextInt();
        int[][] A = new int[N][N];
		int min=Integer.MAX_VALUE;
		for(int i=0;i<N;i++){
		 for(int j=0;j<N;j++){
		 A[i][j]=sc.nextInt();
		 }
		}
		for(int i=0;i<N;i++){
		 for(int j=0;j<N;j++){
		 if(min>A[i][j]){
		 min=A[i][j];
		 }
		 }
		 }
		 System.out.println(min);
		 }
		 }import java.util.Scanner;
