import java.util.Scanner;
public class FCP083{
      public static void main(String[] args){
	  Scanner scn=new Scanner(System.in);
	  int N=scn.nextInt();
	  int s=1;
	  for(int i=1;i<=N;i++){
	     for(int j=1;j<=i;j++){
	  System.out.print(s +" ");
	  s=s+1;
	  }
	  
       System.out.println();
    }
	scn.close();
}	
}	  
	 
	 
	 
